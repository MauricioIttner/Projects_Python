'''
OPERADORES DE COMPARAÇÃO
OP          SIGNIFICADO          EXEMPLO(TRUE)
>             maior                 2 > 1
>=            maior ou igual        2 >= 2
<             menor                 1 < 2
<=            menor ou igual        2 <= 2
==            igual                 'a' == 'a'
!=            diferente             'a' != 'b'
'''

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_igual)
print(igual)
print(diferente)

